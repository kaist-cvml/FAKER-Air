def _add_cmaq_suffix(stats: dict[str, float]) -> dict[str, float]:
    """
    - 2-D  →  <base>_cmaq
    - 3-D  →  <base>_cmaq_<lvl>
    """
    out = {}
    for k, v in stats.items():
        parts = k.split('_')
        if len(parts) == 1:                     # 2-D
            base, lvl = parts[0], None
        else:                                   # 3-D
            *base_parts, lvl = parts
            base = '_'.join(base_parts)

        if lvl is None:                         # 2-D
            out[f"{base}_cmaq"] = v
        else:                                   # 3-D
            out[f"{base}_cmaq_{lvl}"] = v
            if lvl == "sfc":                    # surface → 1013
                out[f"{base}_cmaq_1013"] = v
    return out


